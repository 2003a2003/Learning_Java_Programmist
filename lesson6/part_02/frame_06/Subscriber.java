package lesson6.part_02.frame_06;

public class Subscriber {

    private String name;
    private String mail;

    //konstructor
    public Subscriber() {
    }

    //get - set metods
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

}
